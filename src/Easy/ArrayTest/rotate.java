public class rotate {


    public void rotate(int[] nums, int k) {
        if(nums.length<2||k%nums.length==0){
            return;
        }
        int index=0,first=nums[index],temp;
        for (int i=0;i<nums.length;i++){
            int next=(index+k)%nums.length;
            temp=nums[next];
            nums[next]=first;
            first=temp;
            index=(next+k)%nums.length;
        }
    }


    //环状替换法
    public void rotate2(int[] nums, int k) {
        k = k % nums.length;
        int count = 0;
        for (int start = 0; count < nums.length; start++) {
            int current = start;
            int prev = nums[start];
            do {
                int next = (current + k) % nums.length;
                int temp = nums[next];
                nums[next] = prev;
                prev = temp;
                current = next;
                count++;
            } while (start != current);
        }
    }


    //翻转法
    public void rotate3(int[] nums, int k) {
        k %= nums.length;
        reverse(nums, 0, nums.length - 1);
        reverse(nums, 0, k - 1);
        reverse(nums, k, nums.length - 1);
    }
    public void reverse(int[] nums, int start, int end) {
        while (start < end) {
            int temp = nums[start];
            nums[start] = nums[end];
            nums[end] = temp;
            start++;
            end--;
        }
    }



    public static void main(String[] args) {
        int[] nums=new int[]{1,2,3,4,5,6};
        new rotate().rotate(nums,2);
    }
}
